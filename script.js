function setup() {
  createCanvas(1000, 700);
}

function draw() {
  background(210);
  drawBoard();
  drawPlayers();
}

// Desenează tabla
function drawBoard() {
  fill("white");
  rect(200, 0, 600, 600); 
  fill('brown');
  for (let i = 0; i < 17; i++) {
    for (let j = 0; j < 17; j++) {
      if (i % 2 == 0 && j % 2 == 0) {
        if (a[i][j] != 0) {
          square(288 + i * 25, 80 + j * 25, 40); 
        }
      }
    }
  }
}


function drawPlayers() {
  for (let i = 0; i < 17; i++) {
    for (let j = 0; j < 17; j++) {
      if (a[i][j] == 3) {
        fill('white');
        circle(288 + i * 25 + 20, 80 + j * 25 + 20, 30); // jucător alb
      }
      if (a[i][j] == 4) {
        fill('black');
        circle(288 + i * 25 + 20, 80 + j * 25 + 20, 30); // jucător negru
      }
    }
  }
}